package play;

public class Player {
  private String firstName;
  private String lastName;
  private String suffix;
  private String position;
  private String runYards;
  private String passYards;
  private String recYards;
  private String totalYards;
  private boolean offense;
  private boolean defense;
  private String amountOfInterceptionsGiven;
  private String amountOfFumblesGiven;
  private String amountOfInterceptionsRecieved;
  private String amountOfFumblesRecieved; 
  private String numberOfPenalties;
  private String yardsFromPenalties;
  
  public String getFirstName() {
    return firstName;
  }
  public void setFirstName(String firstName) {
    this.firstName = firstName;
  }
  public String getLastName() {
    return lastName;
  }
  public void setLastName(String lastName) {
    this.lastName = lastName;
  }
  public String getSuffix() {
    return suffix;
  }
  public void setSuffix(String suffix) {
    this.suffix = suffix;
  }
  public String getPosition() {
    return position;
  }
  public void setPosition(String position) {
    this.position = position;
  }
  public String getRunYards() {
    return runYards;
  }
  public void setRunYards(String runYards) {
    this.runYards = runYards;
  }
  public String getPassYards() {
    return passYards;
  }
  public void setPassYards(String passYards) {
    this.passYards = passYards;
  }
  public String getRecYards() {
    return recYards;
  }
  public void setRecYards(String recYards) {
    this.recYards = recYards;
  }
  public String getTotalYards() {
    return totalYards;
  }
  public void setTotalYards(String totalYards) {
    this.totalYards = totalYards;
  }
  public boolean isOffense() {
    return offense;
  }
  public void setOffense(boolean offense) {
    this.offense = offense;
  }
  public boolean isDefense() {
    return defense;
  }
  public void setDefense(boolean defense) {
    this.defense = defense;
  }
  public String getAmountOfInterceptionsGiven() {
    return amountOfInterceptionsGiven;
  }
  public void setAmountOfInterceptionsGiven(String amountOfInterceptionsGiven) {
    this.amountOfInterceptionsGiven = amountOfInterceptionsGiven;
  }
  public String getAmountOfFumblesGiven() {
    return amountOfFumblesGiven;
  }
  public void setAmountOfFumblesGiven(String amountOfFumblesGiven) {
    this.amountOfFumblesGiven = amountOfFumblesGiven;
  }
  public String getAmountOfInterceptionsRecieved() {
    return amountOfInterceptionsRecieved;
  }
  public void setAmountOfInterceptionsRecieved(String amountOfInterceptionsRecieved) {
    this.amountOfInterceptionsRecieved = amountOfInterceptionsRecieved;
  }
  public String getAmountOfFumblesRecieved() {
    return amountOfFumblesRecieved;
  }
  public void setAmountOfFumblesRecieved(String amountOfFumblesRecieved) {
    this.amountOfFumblesRecieved = amountOfFumblesRecieved;
  }
  public String getNumberOfPenalties() {
    return numberOfPenalties;
  }
  public void setNumberOfPenalties(String numberOfPenalties) {
    this.numberOfPenalties = numberOfPenalties;
  }
  public String getYardsFromPenalties() {
    return yardsFromPenalties;
  }
  public void setYardsFromPenalties(String yardsFromPenalties) {
    this.yardsFromPenalties = yardsFromPenalties;
  }
}
